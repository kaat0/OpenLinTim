package net.lintim.util;

import java.util.logging.Level;

/**
 * Enumeration of possible LinTim log levels
 */
public class LogLevel {
    public static Level FATAL = Level.SEVERE;
    public static Level ERROR = Level.SEVERE;
    public static Level WARN = Level.WARNING;
    public static Level INFO = Level.INFO;
    public static Level DEBUG = Level.FINE;
}
